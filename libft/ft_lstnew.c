#include <stdlib.h>
#include "libft.h"

t_list		*ft_lstnew(void const *content, size_t content_size)
{
	t_list	*list;

	list = (t_list *)malloc(sizeof(t_list));
	if (!list)
		return (NULL);
	if (!content)
	{
		list->content = NULL;
		list->content_size = 0;
		return (list);
	}
	list->content = ft_memcpy(malloc(content_size), content, content_size);
	list->content_size = content_size;
	list->next = NULL;
	return (list);
}
