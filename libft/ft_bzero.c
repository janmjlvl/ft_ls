#include "libft.h"
#include <string.h>

void		ft_bzero(void *s, size_t n)
{
	size_t	i;
	char	*ps;

	ps = s;
	i = 0;
	while (i < n)
	{
		ps[i] = '\0';
		i++;
	}
}
